CREATE VIEW [scm].[VW_TB_SCM_RUNWORKINPUT_RESULT_TB]
AS
SELECT [TYPE]
,[PRD_ORDER]
,[SALES_ORDER]
,[MATERIAL]
,[CREATED_ON]
,[RDD_6W]
,[PLAN_RDD]
,[MODIFIED_DUEDATE]
,[QTY]
,[RDD]
   ,CASE WHEN RDD='Holding' THEN NULL
     ELSE DATEPART(ISO_WEEK,CONVERT(DATE,RDD)) END AS RDDWEEK
,[MRP_CTRLR]
,[MG]
,[MODIFIED_WORK_CENTER]
,[CURRENT_OPER_ACT]
,[MANCHINING_OPERATION]
,[LOAD_DT] AS [LOAD_DATE]
,[RDD_WEEK]
FROM [scm].[TB_SCM_RUNWORKINPUT_RESULT_TB]