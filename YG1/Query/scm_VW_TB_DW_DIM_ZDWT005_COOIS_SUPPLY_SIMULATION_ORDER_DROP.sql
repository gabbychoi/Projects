CREATE VIEW [scm].[VW_TB_DW_DIM_ZDWT005_COOIS_SUPPLY_SIMULATION]
AS
SELECT
A.MANDT AS CLIENT,
AUFNR AS [Order],
A.MATNR AS [Material],
SPLSTAT AS [SplitStat],
CONVERT(INT,ERDAT) AS [Created on],
AUFFX AS [Firming],
AUART AS [Order Type],
STAT AS [System Status],
WERKS AS [Plant],
MEINS AS [Unit],
GAMNG AS [Target qty],
WEMNG AS [Del. qty],
IASMG AS [Scrap],
IGMNG AS [Conf. qty],
IAMNG AS [ExpYieldVr],
VERID AS [Version],
MAKTX AS [Material description],
CONVERT(INT,GSTRI)   AS [Act. start],
CONVERT(INT,GLTRI)  AS [Act.finish],
CONVERT(INT,GETRI)  AS [Actual end],
CONVERT(INT,GSUZI)  AS [ActStart],
CONVERT(INT,GEUZI)  AS [Act.finish2],
PKOSA AS [Cost coll.],
REFNR AS [Order2],
CHARG AS [Batch],
STLBEZ AS [BOM material],
NULL AS BOM,
STLAL AS [BOM status],
STLAN AS USAGE,
XLOEK AS [Del. flag],
NULL AS [Pstng Date],
APRIO AS [Priority],
PRCTR AS  [Profit Ctr],
NULL AS [SD Doc.],
KDAUF AS [Sales Ord.],
KDPOS AS [SO Item],
PLNNR AS [Group],
PLNAL AS [Grp.Countr],
NULL AS [Conf. Type],
DISPO AS [MRP ctrlr],
CONVERT(INT,GLTRP) AS [Basic fin.],
MATKL AS [MG],
NULL AS MATERIAL_TYPE,
FERTH AS UNCOATING,
GRDP_005 AS NONCOATING_EDP,
ERSDA AS SIMULATION_DATE
FROM SCM.TB_DW_DIM_ZDWT005_COOIS A
JOIN (SELECT MANDT,MATNR,FERTH,KOSCH,GRDP_005 FROM dbo.TB_DW_DIM_MARA) B
ON A.MATNR=B.MATNR AND A.MANDT=B.MANDT
WHERE DISPO NOT LIKE 'CO%'